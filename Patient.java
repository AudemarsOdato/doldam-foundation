public class Patient {
        public String name;
        public String age;
        public String contact;
        public String appointmentDate;
        public String doctor;

        Patient(String name, String age, String contact, String date, String doctor) {
                this.name = name;
                this.age = age;
                this.contact = contact;
                this.appointmentDate = date;
                this.doctor = doctor;
        }
}
